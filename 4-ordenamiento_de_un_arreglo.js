// Ordenamiento de un arreglo:
// Crea un arreglo de números desordenados y luego ordénalo de menor a mayor utilizando algún algoritmo de ordenamiento
// (p. ej., el método sort()).

// Arreglo desordenado
const numeros = [34, 7, 23, 32, 5, 62, 13];

// Ordenamiento de un arreglo
numeros.sort((a, b) => a - b);

// Mostrar arreglo ordenado con metodo sort
console.log("Arreglo ordenado:", numeros);